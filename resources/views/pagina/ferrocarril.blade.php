            <div class="row g-margin-b-50--xs">
                <div class="col-sm-6 g-margin-b-30--xs g-margin-b-0--sm">
                  <p ALIGN="justify">La historia del Ferrocarril Eléctrico Nacional de Los Altos comenzó el 30 de marzo de 1930. Luego de 81 años de haber dejado de funcionar por órdenes del gobierno de Jorge Ubico (1931-1944), la comunidad altense solo tiene el recuerdo en una melodía en marimba del compositor Domingo Betancur y un museo que guarda documentos y libros con relatos diversos.</p>
                  <P ALIGN="justify">Los periódicos El Tiempo y El Imparcial informaron en su momento que “el eléctrico” funcionó del 30 de marzo de 1930 al 19 septiembre de 1933, tres años en que los quezaltecos disfrutaron en esa obra de la ingeniería alemana. Para los quetzaltecos representó desarrollo económico y un imán para futuros acuerdos políticos en la región, después de la caída del desaparecido Sexto Estado de Los Altos. </p>
                </div>
                <div class="col-sm-6">
                  <p ALIGN="justify">Allgemeine Elektricitäts Gesellschaft fue la empresa que en septiembre de 1924 firmó contrato para construir el ferrocarril eléctrico, cuyo recorrido sería de 45 kilómetros, entre Quezaltenango y San Felipe Retalhuleu, Retalhuleu. En ese punto costero se uniría con la terminal del International Railways of Central America, que en 1912 absorbió el ferrocarril guatemalteco.</p>
                  <P ALIGN="justify">Pasaron 20 años antes de que se construyera el tren eléctrico. En la edificación de la línea férrea trabajaron cientos de pobladores de Quetzaltenango, Cantel, Santa María De Jesús y San Felipe Retalhuleu. A lo largo de la vía se abrió una brecha para el paso del ferrocarril y el túnel de Zunil. El historiador Francisco Cajas Ovando explicó: “la administración del presidente Manuel Estrada Cabrera (1896-1922) apoyó este proyecto sin condiciones.  El 8 de junio de 1921 el Gobierno decretó un impuesto de tres pesos a las bebidas alcohólicas, para la construcción del tranvía”.</p>
                </div>
            </div>
            <div class="g-text-center--xs">
                <p class="g-margin-b-5--xs "ALIGN="justify">La venta de más de diez millones de botellas de licor en todo el país en 25 meses, dejó poco más de 30 millones de pesos en impuestos que sirvieron para la construcción del Ferrocarril Nacional de Los Altos. La recaudación millonaria se hizo gracias al Acuerdo Gubernativo 1119 impuesta al consumo de alcohol.   Cada botella de licor consumida pagaba tres pesos que iban destinados a la mega obra, según el libro Remembranzas del Ferrocarril de Los Altos, del escritor quezalteco Encarnación Soloj Rodas.</p>
