                      <p class="g-font-size-15--sm" ALIGN="justify">En el año 2012, se empezó con el primer gran proyecto de la asociación, Pasos y Pedales, el cual es un programa recreativo que consiste en cerrar cuatro cuadras por algunas horas de la 4ta calle de la zona tres de Quetzaltenango, los días domingos. La finalidad de este programa es para habilitar parques lineales en donde las personas pueden realizar de forma segura y supervisada distintas actividades recreativas como caminatas, andar en bicicleta y el paseo de mascotas. </p>
